char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (s1[j])
		j++;
	while (s2[i])
	{
		s1[j] = (char)s2[i];
		i++;
		j++;
	}
	s1[j] = 0;
	return (s1);
}
