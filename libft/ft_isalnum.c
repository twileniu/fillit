int	ft_isalnum(int c)
{
	int	i;

	i = 0;
	if (c >= '0' && c <= '9')
		i++;
	if ((c <= 'z' && c >= 'a') || (c <= 'Z' && c >= 'A'))
		i++;
	return (i);
}
